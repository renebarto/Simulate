Generator::Generator(Tab *tab, Errors *errors)
{
	this->errors = errors;
	this->tab = tab;
	fram = nullptr;
	gen = nullptr;
    frameFile = {};
}

FILE* Generator::OpenFrame(std::string const & frame)
{
	if (tab->frameDir.length() != 0)
    {
		frameFile = Core::Path::CombinePath(tab->frameDir, frame);
		fram = fopen(frameFile.c_str(), "r");
	}
	if (fram == nullptr)
    {
		frameFile =  Core::Path::CombinePath(tab->srcDir, frame);
		fram = fopen(frameFile.c_str(), "r");
	}
	if (fram == nullptr)
    {
		std::string message = "-- Cannot find : " + frame;
		errors->Exception(message);
	}

	return fram;
}


FILE* Generator::OpenGen(std::string const & genName)
{
	std::string path = Core::Path::CombinePath(tab->outDir, genName);

	if ((gen = fopen(path.c_str(), "r")) != nullptr)
    {
		fclose(gen);
		std::string oldPath = path + ".old";
		remove(oldPath.c_str()); 
        rename(path.c_str(), oldPath.c_str()); // copy with overwrite
	}
	if ((gen = fopen(path.c_str(), "w")) == nullptr)
    {
		std::string message = "-- Cannot generate : " + genName;
		errors->Exception(message);
	}

	return gen;
}


void Generator::GenCopyright()
{
	FILE *file = nullptr;

	if (!tab->frameDir.empty())
    {
		std::string path = Core::Path::CombinePath(tab->frameDir, "Copyright.frame");
		file = fopen(path.c_str(), "r");
	}
	if (file == nullptr)
    {
		std::string path = Core::Path::CombinePath(tab->srcDir, "Copyright.frame");
		file = fopen(path.c_str(), "r");
	}
	if (file == nullptr)
    {
		return;
	}

	FILE * scannerFram = fram;
	fram = file;

	CopyFramePart(L"");
	fram = scannerFram;

	fclose(file);
}

void Generator::GenPrefixFromNamespace()
{
	std::string nsName = tab->nsName;
	if (nsName.empty())
    {
		return;
	}
	const size_t len = nsName.length();
	size_t startPos = 0;
	do
    {
		size_t curLen = String::IndexOf(nsName, startPos, COCO_CPP_NAMESPACE_SEPARATOR);
		if (curLen == std::string::npos)
            curLen = len - startPos;
		std::string curNs = String::Create(nsName, startPos, curLen);
		fwprintf(gen, L"%hs_", curNs.c_str());
		startPos = startPos + curLen + 1;
	} 
    while (startPos < len);
}

void Generator::SkipFramePart(std::wstring const & stop)
{
	CopyFramePart(stop, false);
}

void Generator::CopyFramePart(std::wstring const & stop)
{
	CopyFramePart(stop, true);
}

void Generator::CopyFramePart(std::wstring const & stop, bool generateOutput)
{
	wchar_t startCh = 0;
	size_t endOfStopString = 0;
	wchar_t ch = 0;

	if (!stop.empty())
    {
		startCh = stop[0];
		endOfStopString = stop.length() - 1;
	}

	fwscanf(fram, L"%lc", &ch); //	fram.ReadByte();
	while (!feof(fram))
    { // ch != EOF
		if (!stop.empty() && ch == startCh)
        {
			int i = 0;
			do
            {
				if (i == endOfStopString)
                    return; // stop[0..i] found
				fwscanf(fram, L"%lc", &ch);
                i++;
			} 
            while (ch == stop[i]);
			// stop[0..i-1] found; continue with last read character
			if (generateOutput)
            {
				std::wstring subStop = String::Create(stop, 0, i);
				fwprintf(gen, L"%ls", subStop.c_str());
			}
		} 
        else
        {
			if (generateOutput)
                fwprintf(gen, L"%lc", ch);
			fwscanf(fram, L"%lc", &ch);
		}
	}
	if (!stop.empty())
    {
		std::string message = " -- Incomplete or corrupt frame file: " + frameFile;
		errors->Exception(message);
	}
}

